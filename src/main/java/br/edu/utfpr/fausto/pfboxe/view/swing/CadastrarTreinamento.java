    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        // TODO add your handling code here:
        try{
            if(idAlteracao == 0){
               sessao = sessaoRN.listarUm(Long.valueOf(ses.getSelectedItem().toString()));
               categoria = categoriaRN.listarDesc(ctg.getSelectedItem().toString());
               
               cli = clienteRN.listarUm(Long.valueOf(cc.getText()));
               
               treinamento.setCategoria(categoria);
               treinamento.setCliente(cli);
               treinamento.setSessao(sessao);
               treinamento.setValor(Float.parseFloat(value.getText()));
               treinamento.setPagamento(true);
               treinamento.setDesc(desc.getText());
               treinamento.setTipo(tipos.valueOf(tipo.getSelectedItem().toString()));
               treinamento.setPagamento(pago.isSelected());

               
               treinamentoRN.salvar(treinamento);
               JOptionPane.showMessageDialog(this, "Treinamento cadastrado!");

            }else{
               Treinamento t = treinamentoRN.listarUm(idAlteracao);
               sessao = sessaoRN.listarUm(Long.valueOf(ses.getSelectedItem().toString()));
               categoria = categoriaRN.listarDesc(ctg.getSelectedItem().toString());
               
               cli = clienteRN.listarUm(Long.valueOf(cc.getText()));
               
               t.setCategoria(categoria);
               t.setCliente(cli);
               t.setSessao(sessao);
               t.setValor(Float.parseFloat(value.getText()));
               t.setPagamento(true);
               t.setDesc(desc.getText());
               t.setTipo(tipos.valueOf(tipo.getSelectedItem().toString()));
               t.setPagamento(pago.isSelected());
               
               treinamentoRN.atualizar(t);
               JOptionPane.showMessageDialog(this, "Treinamento atualizado!");                
            }
        } catch(Exception e){
          JOptionPane.showMessageDialog(this, e);

        }
    }//GEN-LAST:event_salvarActionPerformed


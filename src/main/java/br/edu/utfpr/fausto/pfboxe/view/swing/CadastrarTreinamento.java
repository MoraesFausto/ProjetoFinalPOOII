    public void FillBox(){
        try{
            this.categorias  = categoriaRN.listaTodos();
            this.sessoes = sessaoRN.listaTodos();
            
            this.categorias.forEach((c) -> {
                ctg.addItem(c.getDescricao());
             });
            
            this.sessoes.forEach((s)->{
                ses.addItem(String.valueOf(s.getCodigo()));
            });
            
            tipo.setModel(new DefaultComboBoxModel<>(tipoTreinamento.values()));
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }

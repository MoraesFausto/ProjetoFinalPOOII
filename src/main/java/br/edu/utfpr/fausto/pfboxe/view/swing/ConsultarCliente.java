    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        try{
            int linha = this.jtCliente.getSelectedRow();
            Long id = Long.valueOf(jtCliente.getValueAt(linha, 0).toString());
            Cliente cliFind = clienteRN.listarUm(id);
            clienteRN.excluir(cliFind);
            
            DefaultTableModel model = (DefaultTableModel) jtCliente.getModel();
            model.removeRow(linha);
            JOptionPane.showMessageDialog(this, "Cliente Exclu√≠do com Sucesso!");
            this.setClosable(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
        String filtro = codigo.getText();
        
        try {
            DefaultTableModel model = (DefaultTableModel) jtCliente.getModel();
            model.setNumRows(0);
            this.cliente = clienteRN.listarUm(Long.valueOf(filtro));
            
            String[] linha = {String.valueOf( cliente.getCodigo() ), cliente.getNome(), cliente.getCpf(), cliente.getTelefone() };
                model.addRow(linha);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed


